<h1>About Our Youth Ministry</h1>

<span style="text-align:center;">
<h3>First Presbyterian Church of Fairfield</h3>
<p><em>The Mission of the First Presbyterian Church of Fairfield is to lead people to a deeper commitment of faith in God and fellowship with one another, through Jesus Christ. We seek the empowerment of the Holy Spirit to broaden the use of our God-given talents, time and resources to advance the mission outreach of our congregation and the works of the larger Church.</em>
</p>
</span>

<h3>Student Ministry Mission and Values</h3>
<p>The mission of the student ministry of First Presbyterian Church of Fairfield is to provide a welcoming and safe community for its youth and their friends in grades 6-12 where God’s love is proclaimed and faith in Christ is cultivated and deepened so that students are prepared for lives of discipleship and humble service. 
</p>

<p><strong>We value Community. </strong>We strive to be a youth group where everyone feels welcome and is recognized and accepted in the hopes that all might experience God’s grace and unconditional love.</p>

<p><strong>We value Love. </strong>We love because God first loved us, and so we strive to be aware of others’ needs, showing Christ’s compassion, concern, and care for all.</p>

<p><strong>We value God-centeredness. </strong>We are grounded in Scripture and seek to be more like Jesus, living out the fruits of the Spirit, remembering that God is the center of everything we do.
</p>
